# Day 23 - List Analyzer Project

numbers = [10, 25, 5, 40, 15]

print("Numbers in the list:", numbers)

# Find sum
total = sum(numbers)
print("Sum:", total)

# Find maximum
maximum = max(numbers)
print("Maximum:", maximum)

# Find minimum
minimum = min(numbers)
print("Minimum:", minimum)

# Count elements
count = len(numbers)
print("Total elements:", count)
