# Day 34 - Functions in Python

# Simple function
def greet():
    print("Hello, welcome!")

greet()

# Function with parameters
def add(a, b):
    print("Sum:", a + b)

add(5, 3)

# Function with return value
def multiply(a, b):
    return a * b

result = multiply(4, 5)
print("Multiplication:", result)
