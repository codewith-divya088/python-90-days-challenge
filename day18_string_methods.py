# Day 18: String Functions

text = "Python123"

print("Find:", text.find("Python"))
print("Index:", text.index("123"))

print("Is Alpha:", text.isalpha())
print("Is Digit:", text.isdigit())
print("Is Alnum:", text.isalnum())



